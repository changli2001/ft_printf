#include "ft_printf.h"

void	ft_putnbr(long x, int *num)
{
	if (x < 0)
	{
		ft_putchar('-', num);
		x = -x;
	}
	if (x > 9)
		ft_putnbr(x / 10, num);
	ft_putchar((x % 10) + '0', num);
}
